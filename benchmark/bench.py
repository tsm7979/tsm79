#!/usr/bin/env python3
"""
TSM detection-overhead benchmark.

WHAT THIS MEASURES
------------------
The latency TSM adds to a request by running its inline detection primitive:

    PIIDetector().scan(text) -> ScanResult (PII/secret findings + severity)

This is the number that matters for an inline AI firewall: "how much latency
does the guard cost per request?" It is added overhead, single-threaded, on the
machine you run it on. No cluster, no GPU, no cherry-picked hardware.

WHAT THIS DOES NOT MEASURE
--------------------------
- Upstream model latency (hundreds of ms; you don't control it; it dwarfs this).
- Network round-trip to the provider.
- The Rust dataplane. This benchmarks the Python reference implementation that
  ships and runs today. The Rust dataplane targets far lower latency; those are
  design targets until its build is verified (GitHub #36) and benchmarked here.

HOW TO RUN
----------
    python benchmark/bench.py                 # default 5000 iters/category
    python benchmark/bench.py --iters 20000   # tighter percentiles
    python benchmark/bench.py --out benchmark/RESULTS.md
"""
from __future__ import annotations

import argparse
import os
import platform
import statistics
import sys
import time
from datetime import datetime, timezone

# Force UTF-8 stdout so the micro sign / arrows never crash a cp1252 terminal.
try:
    sys.stdout.reconfigure(encoding="utf-8")  # type: ignore[attr-defined]
except Exception:
    pass

from tsm.detectors.pii import PIIDetector

_PAD = (
    " The engineering team reviewed the quarterly roadmap and discussed the "
    "migration plan for the analytics pipeline, including capacity, on-call "
    "rotation, and the rollout schedule across the three regional clusters. "
) * 6  # ~1.1 KB of benign filler

CORPUS: dict[str, list[str]] = {
    "clean": [
        "Summarise the Q3 board deck into five concise bullet points.",
        "Write a Python function that reverses a linked list.",
        "Explain the CAP theorem to a new backend engineer." + _PAD,
    ],
    "pii": [
        "My SSN is 123-45-6789 and my email is alice@example.com.",
        "Call me at 415-555-0132 or reach me at bob@corp.io for the invoice.",
        "Customer record: 4111 1111 1111 1111, DOB 1990-04-12." + _PAD,
    ],
    "secret": [
        "Deploy with GITHUB_TOKEN=ghp_DEMO_FIXTURE_NOT_A_REAL_TOKEN_ab12cd34ef",
        "export AWS creds AKIA_DEMO_FIXTURE_AB for the nightly S3 sync job.",
        "Here is the key: sk-proj-DEMO_FIXTURE_NOT_REAL_abcdef0123456789." + _PAD,
    ],
    "mixed": [
        "Email alice@example.com the SSN 123-45-6789 and the card 4111 1111 1111 1111.",
        "Creds: AKIA_DEMO_FIXTURE_AB and token ghp_DEMO_FIXTURE_NOT_A_REAL_TOKEN_ab" + _PAD,
    ],
}

WARMUP = 300


def _pct(sorted_ns: list[int], pct: float) -> int:
    if not sorted_ns:
        return 0
    k = max(0, min(len(sorted_ns) - 1, int(round(pct / 100.0 * (len(sorted_ns) - 1)))))
    return sorted_ns[k]


def _us(ns: float) -> str:
    return f"{ns / 1000.0:,.1f}"


def _sev(result) -> str:
    ws = getattr(result, "worst_severity", None)
    if ws is None:
        return "none"
    return str(getattr(ws, "value", ws)).lower().replace("severity.", "")


def warm_up(detector: PIIDetector, iters: int = WARMUP) -> None:
    mix = [p for prompts in CORPUS.values() for p in prompts]
    for i in range(iters):
        detector.scan(mix[i % len(mix)])


def bench_category(detector: PIIDetector, prompts: list[str], iters: int) -> dict:
    samples: list[int] = []
    sev_mix: dict[str, int] = {}
    n = len(prompts)
    for i in range(iters):
        text = prompts[i % n]
        t0 = time.perf_counter_ns()
        result = detector.scan(text)
        t1 = time.perf_counter_ns()
        samples.append(t1 - t0)
        s = _sev(result)
        sev_mix[s] = sev_mix.get(s, 0) + 1
    samples.sort()
    mean = statistics.fmean(samples)
    return {
        "mean_ns": mean,
        "p50_ns": _pct(samples, 50),
        "p90_ns": _pct(samples, 90),
        "p99_ns": _pct(samples, 99),
        "max_ns": samples[-1],
        "rps": 1e9 / mean if mean else 0.0,
        "sev_mix": sev_mix,
    }


def fingerprint() -> dict:
    return {
        "platform": platform.platform(),
        "processor": platform.processor() or "unknown",
        "python": platform.python_version(),
        "impl": platform.python_implementation(),
        "cpus": str(os.cpu_count()),
    }


def render_md(results: dict, fp: dict, iters: int) -> str:
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    out = [
        "# TSM Benchmark Results",
        "",
        "> Generated by `python benchmark/bench.py`. **Measured numbers** for the",
        "> **Python reference implementation** on the machine below: the added",
        "> latency of the inline detection primitive (`PIIDetector.scan`).",
        "> Not upstream model latency. Not the Rust dataplane.",
        "",
        f"- **Run:** {now}",
        f"- **Iterations/category:** {iters:,}",
        f"- **Platform:** {fp['platform']}",
        f"- **Processor:** {fp['processor']}",
        f"- **Python:** {fp['impl']} {fp['python']} | **CPUs:** {fp['cpus']}",
        "- **Concurrency:** single thread",
        "",
        "## Added latency per request (lower is better)",
        "",
        "| Category | p50 (µs) | p90 (µs) | p99 (µs) | mean (µs) | req/s (1 core) | severity mix |",
        "|---|---:|---:|---:|---:|---:|---|",
    ]
    for cat, r in results.items():
        v = ", ".join(f"{k}:{n}" for k, n in sorted(r["sev_mix"].items()))
        out.append(
            f"| {cat} | {_us(r['p50_ns'])} | {_us(r['p90_ns'])} | {_us(r['p99_ns'])} | "
            f"{_us(r['mean_ns'])} | {r['rps']:,.0f} | {v} |"
        )
    agg = statistics.fmean([r["mean_ns"] for r in results.values()])
    out += [
        "",
        f"**Aggregate mean added latency:** {_us(agg)} µs/request "
        f"(~{1e9 / agg:,.0f} req/s, single core).",
        "",
        "## Methodology",
        "",
        "- Inline detection primitive per iteration: `PIIDetector().scan(text)`.",
        "- Timing via `time.perf_counter_ns()` around that call only.",
        f"- {WARMUP} warm-up iterations discarded (import / regex compile / cold cache excluded).",
        "- Percentiles are nearest-rank over the full sample set.",
        "- Single-threaded by design; multiply by core count for a first-order throughput estimate.",
        "- Severity column proves real work: clean->none, pii/secret/mixed->critical.",
        "",
        "## Honest caveats",
        "",
        "- Python reference implementation. The Rust dataplane targets lower latency;",
        "  those numbers are design targets until its build is verified (GitHub #36).",
        "- Numbers vary with hardware. Run it on your own box.",
        "- This is *added* latency. Upstream model latency is unaffected and not shown.",
        "",
    ]
    return "\n".join(out)


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description="TSM detection-overhead benchmark")
    ap.add_argument("--iters", type=int, default=5000, help="iterations per category")
    ap.add_argument("--out", default="benchmark/RESULTS.md", help="markdown output path")
    args = ap.parse_args(argv)

    print("TSM detection-overhead benchmark")
    print("  path: PIIDetector().scan()")
    print(f"  iterations/category: {args.iters:,}\n")

    detector = PIIDetector()
    print("  warming up...")
    warm_up(detector)

    results: dict[str, dict] = {}
    for cat, prompts in CORPUS.items():
        print(f"  benchmarking {cat:10} ...", end="", flush=True)
        r = bench_category(detector, prompts, args.iters)
        results[cat] = r
        print(f" p50={_us(r['p50_ns'])}us  p99={_us(r['p99_ns'])}us  {r['rps']:,.0f} req/s")

    fp = fingerprint()
    with open(args.out, "w", encoding="utf-8", newline="\n") as f:
        f.write(render_md(results, fp, args.iters))
    print(f"\n  wrote {args.out}")

    agg = statistics.fmean([r["mean_ns"] for r in results.values()])
    print("=" * 64)
    print(f"  aggregate mean added latency: {_us(agg)} us/request "
          f"(~{1e9 / agg:,.0f} req/s, 1 core)")
    print("=" * 64)
    return 0


if __name__ == "__main__":
    sys.exit(main())
